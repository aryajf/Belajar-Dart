void main() {
  // rumus keliling persegi
  int sisi = 2;
  int kelPersegi = sisi * 2;
  print(kelPersegi);

  // rumus luas lingkaran
  int jari2 = 5;
  double luasLingkaran = 3.14 * jari2 * jari2;
  print(luasLingkaran);

  // rumus volume balok
  int panjang = 2;
  int lebar = 3;
  int tinggi = 4;
  int volBalok = panjang * lebar * tinggi;
  print(volBalok);
}
