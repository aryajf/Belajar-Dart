void main() {
  String nilai = 'A';
  switch (nilai) {
    case 'A':
      print('Sangat baik');
      break;
    case 'B':
      print('Baik');
      break;
    case 'C':
      print('Cukup');
      break;
    case 'D':
      print('Kurang');
      break;
    case 'E':
      print('Sangat Kurang');
      break;
    default:
      print('Tidak valid');
      break;
  }
}
